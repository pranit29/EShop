        UserManager<IdentityUser> _userManager;
        SignInManager<IdentityUser> _signInManager;
        ILogger<RegisterModel> _logger;
        IEmailSender _emailSender;
        ApplicationDbContext _db;
        public UserController(UserManager<IdentityUser> userManager, SignInManager<IdentityUser> signInManager,ILogger<RegisterModel> logger,IEmailSender emailSender,ApplicationDbContext db)
        {
            _userManager = userManager;
